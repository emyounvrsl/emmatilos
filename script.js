// ========== PROJECTS GRID WITH PAGINATION ==========
let currentProjectPage = 1;
const PROJECTS_PER_PAGE = 3;
let filteredProjects = [...projects];

function renderProjectsGrid() {
    const grid = document.getElementById('projectsGrid');
    if (!grid) return;

    const startIndex = (currentProjectPage - 1) * PROJECTS_PER_PAGE;
    const endIndex = startIndex + PROJECTS_PER_PAGE;
    const projectsToShow = filteredProjects.slice(startIndex, endIndex);

    grid.innerHTML = projectsToShow.map((project, index) => `
        <div class="project-card" onclick="openProjectModal(${filteredProjects.indexOf(project)})">
            <div class="project-image">
                ${project.video ? `
                    <video class="project-video" autoplay muted loop playsinline poster="${project.image}">
                        <source src="${project.video}" type="video/mp4">
                    </video>
                ` : `
                    <img src="${project.image}" alt="${project.title}" class="project-screenshot" />
                `}
                <div class="project-overlay">View Details</div>
            </div>
            <div class="project-content">
                <span class="project-category">${project.category}</span>
                <h3>${project.title}</h3>
                <p>${project.description.substring(0, 110)}...</p>
                
                <div class="project-tags">
                    ${project.tags.map(tag => `<span class="tag">${tag}</span>`).join('')}
                </div>

                ${project.figmaLink ? `
                <a href="${project.figmaLink}" target="_blank" class="figma-btn" onclick="event.stopImmediatePropagation()">
                    <i class="fab fa-figma"></i> Try Live Prototype
                </a>` : ''}
            </div>
        </div>
    `).join('');

    renderProjectsPagination();

    // Ensure project videos autoplay (muted required by most browsers).
    // Add debug logging, click-to-play fallback, and intersection observer to start when visible.
    setTimeout(() => {
        const projectVideos = Array.from(grid.querySelectorAll('.project-video'));

        // Helper to try play and log errors
        function tryPlay(video) {
            video.muted = true;
            video.loop = true;
            video.playsInline = true;
            video.autoplay = true;
            try {
                video.load();
                const playPromise = video.play();
                if (playPromise && typeof playPromise.then === 'function') {
                    playPromise.then(() => {
                        console.log('Video started:', video.currentSrc || video.querySelector('source')?.src);
                    }).catch(err => {
                        console.warn('Autoplay prevented for', video, err);
                    });
                }
            } catch (err) {
                console.warn('Error attempting to play video', err);
            }
        }

        // Click-to-play fallback: user gesture will allow playback
        projectVideos.forEach(v => {
            // show a visible play hint if autoplay fails
            v.style.cursor = 'pointer';
            v.addEventListener('click', (e) => {
                e.stopPropagation();
                if (v.paused) {
                    v.play().catch(err => console.warn('Play failed on click', err));
                } else {
                    v.pause();
                }
            });

            v.addEventListener('error', (ev) => {
                console.error('Video error', ev, v.currentSrc || v.querySelector('source')?.src);
            });
        });

        // Use IntersectionObserver to attempt play when the video becomes visible.
        if ('IntersectionObserver' in window) {
            const io = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    const vid = entry.target;
                    if (entry.isIntersecting) {
                        tryPlay(vid);
                    } else {
                        // Optionally pause when not visible to save resources
                        try { vid.pause(); } catch (e) {}
                    }
                });
            }, { threshold: 0.25 });

            projectVideos.forEach(v => io.observe(v));
        } else {
            // Fallback: try to play all videos
            projectVideos.forEach(v => tryPlay(v));
        }
    }, 50);
}

function renderProjectsPagination() {
    const pagination = document.getElementById('projectsPagination');
    if (!pagination) return;

    const totalPages = Math.ceil(filteredProjects.length / PROJECTS_PER_PAGE);

    let paginationHTML = '';
    
    for (let i = 1; i <= totalPages; i++) {
        paginationHTML += `
            <button class="page-btn ${i === currentProjectPage ? 'active' : ''}" 
                    onclick="changeProjectPage(${i})">${i}</button>
        `;
    }
    
    pagination.innerHTML = paginationHTML;
}

function changeProjectPage(page) {
    const totalPages = Math.ceil(filteredProjects.length / PROJECTS_PER_PAGE);
    if (page < 1 || page > totalPages) return;
    
    currentProjectPage = page;
    renderProjectsGrid();
    
    const projectsSection = document.getElementById('projects');
    if (projectsSection) {
        projectsSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
}

function filterProjects(category) {
    currentProjectPage = 1;
    
    if (category === 'all') {
        filteredProjects = [...projects];
    } else {
        filteredProjects = projects.filter(p => p.category === category);
    }
    
    renderProjectsGrid();
}

function openProjectModal(index) {
    const project = filteredProjects[index];
    const modal = document.getElementById('projectModal');
    if (!modal) return;

    const imgEl = document.getElementById('modalProjectImage');
    const videoEl = document.getElementById('modalProjectVideo');

    document.getElementById('modalProjectTitle').textContent = project.title;
    document.getElementById('modalProjectCategory').textContent = project.category.toUpperCase();
    document.getElementById('modalProjectDescription').textContent = project.description;
    const tagsHTML = project.tags.map(tag => `<span class="modal-tag">${tag}</span>`).join('');
    document.getElementById('modalProjectTags').innerHTML = tagsHTML;

    // If project has a video, show the modal video and autoplay it. Otherwise show the image.
    if (project.video) {
        try {
            if (imgEl) imgEl.style.display = 'none';
            if (videoEl) {
                // remove previous sources
                while (videoEl.firstChild) videoEl.removeChild(videoEl.firstChild);
                const src = document.createElement('source');
                src.src = project.video;
                src.type = 'video/mp4';
                videoEl.appendChild(src);
                videoEl.style.display = 'block';
                videoEl.muted = false;
                videoEl.controls = true;
                videoEl.load();
                videoEl.play().catch(err => console.warn('Modal video play prevented', err));
            }
        } catch (e) {
            console.warn('Error preparing modal video', e);
        }
    } else {
        if (videoEl) {
            // hide and clear video
            try { videoEl.pause(); } catch (e) {}
            videoEl.style.display = 'none';
            while (videoEl.firstChild) videoEl.removeChild(videoEl.firstChild);
        }
        if (imgEl) {
            imgEl.src = project.image || '';
            imgEl.style.display = 'block';
        }
    }

    modal.classList.add('open');
    document.body.style.overflow = 'hidden';
}

function closeProjectModal() {
    const modal = document.getElementById('projectModal');
    if (modal) {
        // pause and reset any modal video
        const videoEl = document.getElementById('modalProjectVideo');
        const imgEl = document.getElementById('modalProjectImage');
        if (videoEl) {
            try {
                videoEl.pause();
                videoEl.currentTime = 0;
                while (videoEl.firstChild) videoEl.removeChild(videoEl.firstChild);
                videoEl.style.display = 'none';
            } catch (e) {}
        }
        if (imgEl) imgEl.style.display = 'block';

        modal.classList.remove('open');
        document.body.style.overflow = 'auto';
    }
}

