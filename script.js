function renderActivities() {
    const grid = document.getElementById('activitiesGrid');
    if (!grid) return;

    const startIndex = (activityCurrentPage - 1) * ACTIVITY_ITEMS_PER_PAGE;
    const endIndex = startIndex + ACTIVITY_ITEMS_PER_PAGE;
    const activitiesToShow = activities.slice(startIndex, endIndex);

    grid.innerHTML = activitiesToShow.map((activity, index) => `
        <div class="activity-card" style="animation-delay: ${index * 0.1}s" onclick="openActivityModal(${startIndex + index})">
            <div class="activity-image">
                <img src="${activity.image}" alt="${activity.title}" />
                <div class="activity-overlay">Click for Details</div>
            </div>
            <div class="activity-content">
                ${activity.video ? '<span class="activity-media-tag">Video</span>' : ''}
                <span class="activity-category">${activity.category}</span>
                <h3>${activity.title}</h3>
                <p>${activity.description}</p>
            </div>
        </div>
    `).join('');

    renderActivityPagination();
}

function renderActivityPagination() {
    const pagination = document.getElementById('activitiesPagination');
    if (!pagination) return;

    const totalPages = Math.ceil(activities.length / ACTIVITY_ITEMS_PER_PAGE);
    pagination.innerHTML = `
        <button class="page-btn" id="prevActivitiesBtn" ${activityCurrentPage === 1 ? 'disabled' : ''}>Previous</button>
        <button class="page-btn active" disabled>Page ${activityCurrentPage} of ${totalPages}</button>
        <button class="page-btn" id="nextActivitiesBtn" ${activityCurrentPage === totalPages ? 'disabled' : ''}>Next</button>
    `;

    document.getElementById('prevActivitiesBtn')?.addEventListener('click', () => {
        if (activityCurrentPage > 1) {
            activityCurrentPage -= 1;
            renderActivities();
            window.scrollTo({ top: document.getElementById('activities').offsetTop - 120, behavior: 'smooth' });
        }
    });

    document.getElementById('nextActivitiesBtn')?.addEventListener('click', () => {
        const totalPages = Math.ceil(activities.length / ACTIVITY_ITEMS_PER_PAGE);
        if (activityCurrentPage < totalPages) {
            activityCurrentPage += 1;
            renderActivities();
            window.scrollTo({ top: document.getElementById('activities').offsetTop - 120, behavior: 'smooth' });
        }
    });
}

function openActivityModal(index) {
    const activity = activities[index];
    const modal = document.getElementById('activityModal');
    if (!modal) return;

    const modalImage = document.getElementById('modalActivityImage');
    const modalVideo = document.getElementById('modalActivityVideo');
    const modalVideoSource = document.getElementById('modalActivityVideoSource');

    if (activity.video) {
        modalImage.style.display = 'none';
        modalVideo.style.display = 'block';
        modalVideoSource.src = activity.video;
        modalVideo.load();
    } else {
        modalVideo.pause();
        modalVideo.style.display = 'none';
        modalVideoSource.src = '';
        modalImage.style.display = 'block';
        modalImage.src = activity.image;
    }

    modalImage.src = activity.image;
    document.getElementById('modalActivityTitle').textContent = activity.title;
    document.getElementById('modalActivityDescription').textContent = activity.description;
    const link = document.getElementById('modalActivityLink');
    if (link) {
        link.href = activity.link;
    }
    
    modal.classList.add('open');
    document.body.style.overflow = 'hidden';
}

function closeActivityModal() {
    const modal = document.getElementById('activityModal');
    const modalVideo = document.getElementById('modalActivityVideo');
    const modalVideoSource = document.getElementById('modalActivityVideoSource');

    if (modal) {
        modal.classList.remove('open');
        document.body.style.overflow = 'auto';
    }

    if (modalVideo) {
        modalVideo.pause();
    }
    if (modalVideoSource) {
        modalVideoSource.src = '';
    }
}

