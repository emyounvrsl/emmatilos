let currentSkillsPage = 1;
const SKILLS_PER_PAGE = 3;
let filteredSkills = [...skills];

function renderSkillsGrid() {
    const grid = document.getElementById('skillsGrid');
    const startIndex = (currentSkillsPage - 1) * SKILLS_PER_PAGE;
    const endIndex = startIndex + SKILLS_PER_PAGE;
    const skillsToShow = filteredSkills.slice(startIndex, endIndex);

    grid.innerHTML = skillsToShow.map((skills, index) => `
        <div class="skills-card" style="animation-delay: ${index * 0.1}s" onclick="openSkillsModal(${filteredSkills.indexOf(skills)})">
            <div class="skills-image">
                <div class="badge-icon-placeholder">
                    ${skills.image ? `<img src="${skills.image}" alt="${skills.title}" class="skills-screenshot" />` : `<span class="badge-placeholder-icon">🏅</span>`}
                </div>
                <div class="skills-overlay">Click for Details</div>
            </div>
            <div class="skills-content">
                <span class="skills-category">${skills.category}</span>
                <h3>${skills.title}</h3>
                <p>${skills.description.substring(0, 100)}...</p>
                <div class="skills-tags">
                    ${skills.tags.map(tag => `<span class="tag">${tag}</span>`).join('')}
                </div>
            </div>
        </div>
    `).join('');

    renderSkillsPagination();
}

function renderSkillsPagination() {
    const pagination = document.getElementById('skillsPagination');
    const totalPages = Math.ceil(filteredSkills.length / SKILLS_PER_PAGE);

    let paginationHTML = '';
    
    for (let i = 1; i <= totalPages; i++) {
        paginationHTML += `
            <button class="page-btn ${i === currentSkillsPage ? 'active' : ''}" 
                    onclick="changeSkillsPage(${i})">${i}</button>
        `;
    }
    
    pagination.innerHTML = paginationHTML;
}

function changeSkillsPage(page) {
    const totalPages = Math.ceil(filteredSkills.length / SKILLS_PER_PAGE);
    if (page < 1 || page > totalPages) return;
    
    currentSkillsPage = page;
    renderSkillsGrid();
    
    document.getElementById('skills').scrollIntoView({ behavior: 'smooth', block: 'start' });
}

function filterSkills(category) {
    currentSkillsPage = 1;
    
    if (category === 'all') {
        filteredSkills = [...skills];
    } else {
        filteredSkills = skills.filter(p => p.category === category);
    }
    
    renderSkillsGrid();
}

function openSkillsModal(index) {
    const skills = filteredSkills[index];
    const imgEl = document.getElementById('modalSkillsImage');
    const videoEl = document.getElementById('modalSkillsVideo');

    document.getElementById('modalSkillsTitle').textContent = skills.title;
    document.getElementById('modalSkillsCategory').textContent = skills.category.toUpperCase();
    document.getElementById('modalSkillsDescription').textContent = skills.description;
    const tagsHTML = skills.tags.map(tag => `<span class="modal-tag">${tag}</span>`).join('');
    document.getElementById('modalSkillsTags').innerHTML = tagsHTML;

    // If skill has a video, show video player; otherwise show image
    if (skills.video) {
        if (imgEl) imgEl.style.display = 'none';
        if (videoEl) {
            // clear previous sources
            while (videoEl.firstChild) videoEl.removeChild(videoEl.firstChild);
            const src = document.createElement('source');
            src.src = skills.video;
            src.type = 'video/mp4';
            videoEl.appendChild(src);
            videoEl.style.display = 'block';
            videoEl.controls = true;
            videoEl.load();
            // attempt to play, but don't require it
            videoEl.play().catch(() => {});
        }
    } else {
        if (videoEl) {
            try { videoEl.pause(); } catch (e) {}
            videoEl.style.display = 'none';
            while (videoEl.firstChild) videoEl.removeChild(videoEl.firstChild);
        }
        if (imgEl) {
            imgEl.src = skills.image || '';
            imgEl.style.display = 'block';
        }
    }

    document.getElementById('skillsModal').classList.add('open');
    document.body.style.overflow = 'hidden';
}

function closeSkillsModal() {
    const videoEl = document.getElementById('modalSkillsVideo');
    if (videoEl) {
        try { videoEl.pause(); videoEl.currentTime = 0; } catch (e) {}
        while (videoEl.firstChild) videoEl.removeChild(videoEl.firstChild);
        videoEl.style.display = 'none';
    }
    const imgEl = document.getElementById('modalSkillsImage');
    if (imgEl) imgEl.style.display = 'block';

    document.getElementById('skillsModal').classList.remove('open');
    document.body.style.overflow = 'auto';
}

